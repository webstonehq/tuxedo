use ratatui::style::Color;

#[derive(Debug, Clone, Copy)]
pub struct Theme {
    pub name: &'static str,
    pub bg: Color,
    pub panel: Color,
    pub border: Color,
    pub fg: Color,
    pub dim: Color,
    pub accent: Color,
    pub cursor: Color,
    #[allow(dead_code)]
    pub selection: Color,
    pub statusbar: Color,
    pub status_fg: Color,
    pub mode_fg: Color,
    pub mode_bg: Color,
    pub pri_a: Color,
    pub pri_b: Color,
    pub pri_c: Color,
    pub pri_d: Color,
    pub pri_other: Color,
    pub project: Color,
    pub context: Color,
    pub due: Color,
    pub overdue: Color,
    pub today: Color,
    pub done: Color,
    pub selected: Color,
    pub matched: Color,
}

impl Theme {
    pub fn priority_color(&self, p: char) -> Color {
        match p {
            'A' => self.pri_a,
            'B' => self.pri_b,
            'C' => self.pri_c,
            'D' => self.pri_d,
            _ => self.pri_other,
        }
    }
}

const fn rgb(r: u8, g: u8, b: u8) -> Color {
    Color::Rgb(r, g, b)
}

